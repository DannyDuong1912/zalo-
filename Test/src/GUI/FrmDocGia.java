private void loadbangdocgia() {
    DefaultTableModel model = new DefaultTableModel();
    Object[] title={"Mã","Tên độc giả","ngày sinh","giới tính","Địa chỉ","số điện thoại"};
    model.setColumnIdentifiers(title);
    //Dưa các dòng dữ liệu từ list danh sách độc giả lên
    danhSachDocGia= new DAO.DAODocGia().layDanhSachDocGia();
    for (DocGia dg : danhSachDocGia) {
    Object[] row = {dg.getMa(),dg.getTen(),dateFormat.format(dg.getNgaySinh()),dg.getGioiTinh(),dg.getDiaChi(),dg.getSoDienThoai()};
    model.addRow(row);
    }
    
    tabledocgia.setModel(model);
    
}

    private void tabledocgiaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabledocgiaMouseClicked
        int vitriclick = tabledocgia.getSelectedRow();
        txtma.setText(tabledocgia.getValueAt(vitriclick, 0).toString());
        txtten.setText(tabledocgia.getValueAt(vitriclick, 1).toString());
        try {
            datengaysinh.setDate(dateFormat.parse(tabledocgia.getValueAt(vitriclick, 2).toString()));
        } catch (Exception e) {
            e.printStackTrace();
        }
        cbxgioitinh.setSelectedItem(tabledocgia.getValueAt(vitriclick, 3));
        txtdiachi.setText(tabledocgia.getValueAt(vitriclick, 4).toString());
        txtsdt.setText(tabledocgia.getValueAt(vitriclick, 5).toString());
    }//GEN-LAST:event_tabledocgiaMouseClicked

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        DocGia dg = new DocGia();
        dg.setMa(txtma.getText());
        dg.setTen(txtten.getText());
        dg.setNgaySinh(datengaysinh.getDate());
        dg.setGioiTinh(cbxgioitinh.getSelectedItem().toString());
        dg.setDiaChi(txtdiachi.getText());
        dg.setSoDienThoai(txtsdt.getText());
        if(new DAODocGia().update(dg)){
            JOptionPane.showMessageDialog(rootPane, "Update thành công");
            loadbangdocgia();
        }
         
            
    }//GEN-LAST:event_btnsuaActionPerformed

